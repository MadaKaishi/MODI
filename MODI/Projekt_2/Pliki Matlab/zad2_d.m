
f = @(u,y) (W(1)+W(5)+W(9))*u +... 
    (W(2)+W(6)+W(10))*u.^2 +...
    (W(3)+W(7)+W(11))*u.^3 +...
    (W(4)+W(8)+W(12))*u.^4 +...
    (W(13)+W(17)+W(21)-1)*y +... 
    (W(14)+W(18)+W(22))*y.^2 +... 
    (W(15)+W(19)+W(23))*y.^3 +...
    (W(16)+W(20)+W(24))*y.^4;

G = linspace(-1,1, 1000).';
for i=1:length(G)
wart = fsolve(@(y)f(G(i),y),[0,1]);
y_wyj(i,:) = wart;
end 

plot(G,y_wyj(:,1))
title("Charakterystyka y(u)")
xlabel("u"); ylabel("y");
hold off;
print("Charakterystyka",'-dpng','-r400');